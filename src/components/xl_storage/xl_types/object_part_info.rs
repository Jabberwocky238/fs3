use serde::{Deserialize, Serialize};
use std::collections::HashMap;

use super::msgpack_compat::{MsgpackReader, MsgpackWriter};

#[derive(Debug, Clone, Serialize, Deserialize)]
pub struct ObjectPartInfo {
    pub etag: Option<String>,
    pub number: i32,
    pub size: i64,
    pub actual_size: i64,
    pub mod_time: i64,
    pub index: Option<Vec<u8>>,
    pub crc: Option<HashMap<String, String>>,
    pub error: Option<String>,
}

impl From<Vec<u8>> for ObjectPartInfo {
    fn from(bytes: Vec<u8>) -> Self {
        let r = MsgpackReader::new(&bytes);
        Self {
            etag: r.get_str("e"),
            number: r.get_i64("n").unwrap() as i32,
            size: r.get_i64("s").unwrap(),
            actual_size: r.get_i64("as").unwrap(),
            mod_time: r.get_go_time("mt").unwrap(),
            index: r.get_bytes("i"),
            crc: r.get_map("crc").map(|v| v.into_iter().collect()),
            error: r.get_str("err"),
        }
    }
}

impl From<ObjectPartInfo> for Vec<u8> {
    fn from(val: ObjectPartInfo) -> Self {
        let mut w = MsgpackWriter::new();

        let field_count = 5 + val.index.is_some() as u32 + val.crc.is_some() as u32 + val.error.is_some() as u32;
        w.write_map_len(field_count);

        w.write_str_field("e", val.etag.as_deref().unwrap_or(""));
        w.write_int64_field("n", val.number as i64);
        w.write_int16_field("s", val.size);
        w.write_int16_field("as", val.actual_size);
        w.write_go_time_field("mt", val.mod_time);

        if let Some(ref idx) = val.index {
            w.write_bin_field("i", idx);
        }
        if let Some(ref crc) = val.crc {
            w.write_map_field("crc", crc.len() as u32, |w| {
                for (k, v) in crc {
                    w.write_str_field(k, v);
                }
            });
        }
        if let Some(ref err) = val.error {
            w.write_str_field("err", err);
        }

        w.finish()
    }
}

